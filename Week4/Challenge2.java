/*
Write a program that takes text and prints the length (number of characters)
of the text to the screen.
*/

import java.util.*;

public class Solution {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        String s= in.next();//University
        int a = s.length(); //University == 10
        System.out.println(a);
    }
}
